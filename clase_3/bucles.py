# listas
frutas = ["manzana", "mango", "fresa", "coco", "kiwi"]
for fruta in frutas:
    print(fruta)

# tuplas 
verduras = ("coliflor", "espinaca", "brocoli")
for verdura in verduras:
    print(verdura)

# rangos 
for numero in range(10):
    print(numero)

for elemento in range(25, 30):
    print(elemento)
